public class Product {

    private String title;
    private String id;
    private String thumbnail;
    private Float price;

    public String getTitle() {
        return title;
    }

    public String getId() {
        return id;
    }

    public String getThumbnailUrl() {
        return thumbnail;
    }

    public Float getPrice() {
        return price;
    }
}
